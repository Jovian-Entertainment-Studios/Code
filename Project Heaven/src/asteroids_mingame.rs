pub fn asteroidgeneration() -> (f64, f64, f64, f64, u8, u8) {
    //todo: set ranges for random numbers
    //-----------------------------------------------------------------------------------------------------------------------------------------------
    let x_pos = rand::random::<f64>();
    let y_pos = rand::random::<f64>();
    //let z_pos = rand::random::<f64>(); //dont need z
    //-----------------------------------------------------------------------------------------------------------------------------------------------
    let x_vel = rand::random::<f64>();
    let y_vel = rand::random::<f64>();
    //let z_vel = rand::random::<f64>();
    //-----------------------------------------------------------------------------------------------------------------------------------------------
    let asteroid_type = rand::random::<u8>();
    let level = rand::random::<u8>();
    //-----------------------------------------------------------------------------------------------------------------------------------------------
    return (x_pos, y_pos, x_vel, y_vel, asteroid_type, level);
    //-----------------------------------------------------------------------------------------------------------------------------------------------
}
